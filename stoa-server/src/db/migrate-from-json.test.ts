// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

const tmpDirs: string[] = []

function makeTmpDir(): string {
  const dir = mkdtempSync(join(tmpdir(), 'stoa-migration-test-'))
  tmpDirs.push(dir)
  return dir
}

function makeDir(dir: string): void {
  mkdirSync(dir, { recursive: true })
}

function writeJson(filePath: string, data: unknown): Promise<void> {
  return writeFile(filePath, JSON.stringify(data, null, 2), 'utf-8')
}

/** Close the underlying better-sqlite3 connection held by a drizzle wrapper. */
function closeDb(db: ReturnType<typeof createDb>): void {
  const raw = (db as unknown as { $client: { close: () => void } }).$client
  raw.close()
}

afterAll(async () => {
  for (const dir of tmpDirs) {
    await rm(dir, { recursive: true, force: true }).catch(() => {
      // On Windows, WAL files may still be locked by the migration's internal
      // connection which we cannot close (migrateFromJson does not expose it).
      // That is fine — temp dirs are cleaned by the OS eventually.
    })
  }
})

