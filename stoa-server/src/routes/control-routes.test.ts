/**
 * Integration tests for /ctl/ control routes — Phase 2b + Phase 3 stubs.
 *
 * Tests:
 *   GET  /ctl/whoami
 *   GET  /ctl/session/list
 *   GET  /ctl/session/:id/inspect
 *   POST /ctl/session/create
 *
 * Auth validation works via:
 *   - x-stoa-secret (matches ctlSecret) for local-user
 *   - x-stoa-session-id + x-stoa-session-token pair for sessions
 */
import { describe, it, expect, vi } from 'vitest';
import { Hono } from 'hono';
import type { SessionNodeSnapshot, SessionSummary, SessionType } from 'stoa-shared';
import { createControlRoutes, type ControlDeps, type RuntimeBridgeClient } from './control';
import { SessionVisibilityService } from '../services/session-visibility-service';
import { createErrorHandler } from '../middleware/error-handler';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

interface ControlEnvelope<T> {
  ok: boolean;
  data: T;
  error?: { code: string; message: string; details: Record<string, unknown> };
}

async function parseBody<T>(res: Response): Promise<T> {
  return (await res.json()) as T;
}

// ---------------------------------------------------------------------------
// Test fixtures
// ---------------------------------------------------------------------------

const NOW = '2026-01-01T00:00:00.000Z';

