describe('AppShell', () => {
  beforeEach(() => {
    const stoaMock: RendererApi = createRendererApiMock({
      createProject: vi.fn().mockResolvedValue(baseProject),
      createSession: vi.fn().mockResolvedValue(baseSession),
      regenerateSessionTitle: vi.fn().mockResolvedValue(baseSession),
      getAppObservability: vi.fn().mockResolvedValue({
        blockedProjectCount: 0,
        failedProjectCount: 0,
        totalUnreadTurns: 0,
        projectsNeedingAttention: [],
        providerHealthSummary: {},
        lastGlobalEventAt: null,
        sourceSequence: 0,
        updatedAt: '2026-04-21T00:00:00.000Z'
      }),
      getUpdateState: vi.fn().mockResolvedValue({
        phase: 'idle',
        currentVersion: '0.1.0',
        availableVersion: null,
        downloadedVersion: null,
        downloadProgressPercent: null,
        lastCheckedAt: null,
        message: null,
        requiresSessionWarning: false
      }),
      checkForUpdates: vi.fn().mockResolvedValue({
        phase: 'up-to-date',
        currentVersion: '0.1.0',
        availableVersion: null,
        downloadedVersion: null,
        downloadProgressPercent: null,
        lastCheckedAt: null,
        message: 'You are up to date.',
        requiresSessionWarning: false
      }),
      downloadUpdate: vi.fn().mockResolvedValue({
        phase: 'downloaded',
        currentVersion: '0.1.0',
        availableVersion: '0.2.0',
        downloadedVersion: '0.2.0',
        downloadProgressPercent: 100,
        lastCheckedAt: null,
        message: 'Update 0.2.0 is ready to install.',
        requiresSessionWarning: false
      }),
      gitStatus: vi.fn().mockResolvedValue({
        branch: 'main',
        entries: [],
        ahead: 0,
        behind: 0
      }),
      gitBranches: vi.fn().mockResolvedValue({
        current: 'main',
        locals: ['main', 'develop'],
        remotes: []
      }),
      gitLog: vi.fn().mockResolvedValue([]),
      gitStage: vi.fn().mockResolvedValue(undefined),
      gitUnstage: vi.fn().mockResolvedValue(undefined),
      gitDiscard: vi.fn().mockResolvedValue(undefined),
      gitPush: vi.fn().mockResolvedValue(undefined),
      gitPull: vi.fn().mockResolvedValue(undefined),
      gitFetch: vi.fn().mockResolvedValue(undefined),
      gitCheckoutBranch: vi.fn().mockResolvedValue(undefined),
      gitCreateBranch: vi.fn().mockResolvedValue(undefined),
      gitRebase: vi.fn().mockResolvedValue(undefined),
      gitMerge: vi.fn().mockResolvedValue(undefined)
    })

    window.stoa = stoaMock
  })

  it('shows command/sidebar/settings activity items and defaults to command view', () => {
    const wrapper = mountAppShell({
      hierarchy: [],
      activeProjectId: null,
      activeSessionId: null,
      activeProject: null,
      activeSession: null
    })

    const labels = wrapper.findAll('[data-activity-item]').map((node) => node.attributes('data-activity-item'))
    const navigation = wrapper.get('nav[aria-label="Global activity"]')
    const commandButton = wrapper.get('button[aria-label="Command panel"]')
    const settingsButton = wrapper.get('button[aria-label="Settings"]')

    expect(labels).toEqual(['command', 'sidebar-toggle', 'settings'])
    expect(navigation).toBeTruthy()
    expect(commandButton.attributes('aria-current')).toBe('true')
    expect(settingsButton.attributes('aria-current')).toBeUndefined()
    expect(wrapper.find('[data-command-surface="true"]').exists()).toBe(true)
    expect(wrapper.find('[data-surface="command"][aria-label="Command surface"]').exists()).toBe(true)
    expect(wrapper.find('.terminal-empty-state').exists()).toBe(true)
  })

  it('keeps stable command and active-session surface hooks when a session is selected', () => {
    const wrapper = mountAppShell({
      hierarchy: [{
        ...baseProject,
        active: true,
        archivedSessions: [],
        sessions: [{
          ...baseSession,
          active: true
        }]
      }],
      activeProjectId: baseProject.id,
      activeSessionId: baseSession.id,
      activeProject: baseProject,
      activeSession: baseSession
    })

    expect(wrapper.find('[data-surface="command"]').exists()).toBe(true)
    expect(wrapper.find('[data-command-surface="true"]').exists()).toBe(true)
    expect(wrapper.find('.terminal-empty-state').exists()).toBe(false)
    expect(wrapper.find('[data-testid="terminal-xterm"]').exists()).toBe(true)
    expect(wrapper.find('[data-testid="terminal-status-bar"]').exists()).toBe(false)
  })

  it('keeps command surface mounted and hidden when the settings activity is selected', async () => {
    const wrapper = mountAppShell({
      hierarchy: [],
      activeProjectId: null,
      activeSessionId: null,
      activeProject: null,
      activeSession: null
    })

    const commandSurface = wrapper.get('[data-surface="command"][aria-label="Command surface"]')

    await wrapper.get('button[aria-label="Settings"]').trigger('click')

    expect(wrapper.get('[data-surface="settings"][aria-label="Settings surface"]')).toBeTruthy()
    expect(wrapper.get('button[aria-label="Settings"]').attributes('aria-current')).toBe('true')
    expect(wrapper.get('[data-surface="command"][aria-label="Command surface"]').element).toBe(commandSurface.element)
    expect(wrapper.get('[data-surface="command"][aria-label="Command surface"]').attributes('style')).toContain('display: none;')
    expect(wrapper.find('[data-surface="command"][aria-label="Command surface"]').exists()).toBe(true)
    expect(wrapper.findComponent({ name: 'CommandSurface' }).props('visible')).toBe(false)
  })

  it('keeps activity icons rendered while switching surfaces', async () => {
    const wrapper = mountAppShell({
      hierarchy: [],
      activeProjectId: null,
      activeSessionId: null,
      activeProject: null,
      activeSession: null
    })

    const expectStableIcons = () => {
      const items = wrapper.findAll('[data-activity-item]')
      const icons = wrapper.findAll('[data-activity-icon]')

      expect(items).toHaveLength(3)
      expect(icons).toHaveLength(3)
      expect(wrapper.get('[data-activity-item="command"]').find('[data-activity-icon]').exists()).toBe(true)
      expect(wrapper.get('[data-activity-item="sidebar-toggle"]').find('[data-activity-icon]').exists()).toBe(true)
      expect(wrapper.get('[data-activity-item="settings"]').find('[data-activity-icon]').exists()).toBe(true)
    }

    expectStableIcons()

    await wrapper.get('button[aria-label="Settings"]').trigger('click')
    expectStableIcons()

    await wrapper.get('button[aria-label="Command panel"]').trigger('click')
    expectStableIcons()
  })

  it('forwards restoreSession from command surface', async () => {
    const wrapper = mountAppShell({
      hierarchy: [{
        ...baseProject,
        active: false,
        sessions: [],
        archivedSessions: [{
          ...baseSession,
          id: 'session-archived',
          archived: true,
          active: false
        }]
      }],
      activeProjectId: null,
      activeSessionId: null,
      activeProject: null,
      activeSession: null
    })

    await wrapper.findComponent({ name: 'CommandSurface' }).vm.$emit('restoreSession', 'session-archived')

    expect(wrapper.emitted('restoreSession')).toEqual([['session-archived']])
  })

  it('forwards restartSession from command surface', async () => {
    const wrapper = mountAppShell({
      hierarchy: [{
        ...baseProject,
        active: true,
        archivedSessions: [],
        sessions: [{
          ...baseSession,
          active: true
        }]
      }],
      activeProjectId: baseProject.id,
      activeSessionId: baseSession.id,
      activeProject: baseProject,
      activeSession: baseSession
    })

    await wrapper.findComponent({ name: 'CommandSurface' }).vm.$emit('restartSession', 'session-1')

    expect(wrapper.emitted('restartSession')).toEqual([['session-1']])
  })

  it('forwards openWorkspace from command surface', async () => {
    const wrapper = mountAppShell({
      hierarchy: [{
        ...baseProject,
        active: true,
        archivedSessions: [],
        sessions: [{
          ...baseSession,
          active: true
        }]
      }],
      activeProjectId: baseProject.id,
      activeSessionId: baseSession.id,
      activeProject: baseProject,
      activeSession: baseSession
    })

    await wrapper.findComponent({ name: 'CommandSurface' }).vm.$emit('openWorkspace', {
      sessionId: 'session-1',
      target: 'ide'
    })

    expect(wrapper.emitted('openWorkspace')).toEqual([
      [{ sessionId: 'session-1', target: 'ide' }]
    ])
  })

  describe('Sidebar toggle integration', () => {
    it('sidebar is not visible by default', () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const sidebar = wrapper.get('[data-testid="right-sidebar"]')
      expect(sidebar.classes()).toContain('right-sidebar-closed')
    })

    it('clicking sidebar toggle makes sidebar visible', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')
      await toggleButton.trigger('click')

      const sidebar = wrapper.get('[data-testid="right-sidebar"]')
      expect(sidebar.classes()).not.toContain('right-sidebar-closed')
    })

    it('clicking sidebar toggle twice hides sidebar again', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')
      await toggleButton.trigger('click')
      await toggleButton.trigger('click')

      const sidebar = wrapper.get('[data-testid="right-sidebar"]')
      expect(sidebar.classes()).toContain('right-sidebar-closed')
    })

    it('sidebar toggle button reflects sidebar open state', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')

      expect(toggleButton.attributes('aria-pressed')).toBe('false')

      await toggleButton.trigger('click')
      expect(toggleButton.attributes('aria-pressed')).toBe('true')

      await toggleButton.trigger('click')
      expect(toggleButton.attributes('aria-pressed')).toBe('false')
    })

    it('sidebar toggle has correct active styling class', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')

      expect(toggleButton.classes()).not.toContain('text-text-strong')

      await toggleButton.trigger('click')
      expect(toggleButton.classes()).toContain('text-text-strong')

      await toggleButton.trigger('click')
      expect(toggleButton.classes()).not.toContain('text-text-strong')
    })

    it('sidebar toggle does not change the active surface', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const commandButton = wrapper.get('button[aria-label="Command panel"]')
      expect(commandButton.attributes('aria-current')).toBe('true')

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')
      await toggleButton.trigger('click')

      expect(wrapper.find('[data-surface="command"]').exists()).toBe(true)
      expect(commandButton.attributes('aria-current')).toBe('true')
    })

    it('sidebar auto-hides when navigating to a non-command surface', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')
      await toggleButton.trigger('click')
      const sidebar = wrapper.get('[data-testid="right-sidebar"]')
      expect(sidebar.classes()).not.toContain('right-sidebar-closed')

      await wrapper.get('button[aria-label="Settings"]').trigger('click')
      expect(sidebar.classes()).toContain('right-sidebar-closed')
    })

    it('sidebar auto-restores when returning to command surface', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')
      await toggleButton.trigger('click')
      const sidebar = wrapper.get('[data-testid="right-sidebar"]')
      expect(sidebar.classes()).not.toContain('right-sidebar-closed')

      await wrapper.get('button[aria-label="Settings"]').trigger('click')
      expect(sidebar.classes()).toContain('right-sidebar-closed')

      await wrapper.get('button[aria-label="Command panel"]').trigger('click')
      expect(sidebar.classes()).not.toContain('right-sidebar-closed')
    })

    it('sidebar stays hidden on return to command if it was not open before navigation', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      // Sidebar is closed by default
      const sidebar = wrapper.get('[data-testid="right-sidebar"]')
      expect(sidebar.classes()).toContain('right-sidebar-closed')

      await wrapper.get('button[aria-label="Settings"]').trigger('click')
      await wrapper.get('button[aria-label="Command panel"]').trigger('click')

      // Should still be closed -- nothing to restore
      expect(sidebar.classes()).toContain('right-sidebar-closed')
    })

    it('sidebar width matches store default', async () => {
      const wrapper = mountAppShell({
        hierarchy: [],
        activeProjectId: null,
        activeSessionId: null,
        activeProject: null,
        activeSession: null
      })

      const toggleButton = wrapper.get('[data-activity-item="sidebar-toggle"]')
      await toggleButton.trigger('click')

      const sidebar = wrapper.get('[data-testid="right-sidebar"]')
      expect(sidebar.attributes('style')).toContain('280px')
    })
  })
})
